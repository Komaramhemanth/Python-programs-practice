string1 = "Hello, "
string2 = "World!"

# Concatenate strings
concatenated_string = string1 + string2

# Print the concatenated string
print("Concatenated String:", concatenated_string)

# Access and print a substring
start_index = 7
end_index = 12
substring = concatenated_string[start_index:end_index]

